// Helper function to get blog by slug
export const getBlogPostBySlug = (slug: string): BlogPost | undefined => {
  return blogPosts.find(post => post.slug === slug);
};

// Helper function to get blogs by category
export const getBlogsByCategory = (category: string): BlogPost[] => {
  return blogPosts.filter(post => post.category === category);
};

// Helper function to get related blogs
export const getRelatedBlogs = (currentSlug: string, limit: number = 3): BlogPost[] => {
  const current = blogPosts.find(post => post.slug === currentSlug);
  if (!current) return blogPosts.slice(0, limit);
  
  return blogPosts
    .filter(post => post.slug !== currentSlug && post.category === current.category)
    .slice(0, limit);
};